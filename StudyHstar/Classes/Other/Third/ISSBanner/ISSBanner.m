#import "ISSBanner.h"

@implementation ISSBanner

- (NSString *)imageURLString
{
    if (_imageURLString == nil) {
        return  @"";
    }
    return _imageURLString;
}

- (NSString *)title
{
    if (_title == nil) {
        return @"";
    }
    return _title;
}

-(NSString *)commonInfoTitle
{
    return self.title;
}

-(NSString *)commonInfoImageURLString
{
    return self.imageURLString;
}

-(NSString *)commonInfoImageName
{
    return self.imageName;
}

@end